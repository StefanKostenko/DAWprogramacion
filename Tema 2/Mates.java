public class Mates {
    
    public static double suma(double numero1,double numero2){
        double resultado  = 0;
        resultado = numero1 + numero2;

        return resultado;
    }

    public static double resta(double numero1,double numero2){
        double resultado = 0;
        resultado = numero1 - numero2;

        return resultado;
    }

    public static double multiplicacion(double numero1,double numero2){
        double resultado  = 0;
        resultado = numero1 * numero2;

        return resultado;
    }

    public static double division(double numero1,double numero2){
        double resultado  = 0;
        resultado = numero1 / numero2;

        return resultado;
    }

    public static double areaDelCirculo(double radio){
        double resultado = 0;
        resultado = radio*radio * Math.PI;

        return resultado;
    }
}